// sync-docs.test.mjs — Unit and integration tests for feature-005 (task-015)
//
// Tests the migration transform: H1 strip, fence-awareness, link rewrites,
// image relocation, frontmatter injection, and idempotency (drift-check).

import { describe, it, expect, beforeAll } from 'vitest';
import { readFileSync, existsSync } from 'node:fs';
import { resolve, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execSync } from 'node:child_process';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SITE_ROOT = resolve(__dirname, '../../');
const REPO_ROOT = resolve(__dirname, '../../../');
const CONTENT_DOCS = join(SITE_ROOT, 'src', 'content', 'docs');
const ASSETS_DIR = join(SITE_ROOT, 'src', 'assets');
const MANIFEST_PATH = join(SITE_ROOT, 'scripts', '.synced-manifest.json');

// The four migrated pages
const MIGRATED_PAGES = [
  {
    dest: join(CONTENT_DOCS, 'concepts', 'methodology.md'),
    src: join(REPO_ROOT, 'docs', 'aid-methodology.md'),
    frontmatter: { title: 'The AID Methodology', sourceDoc: 'docs/aid-methodology.md' },
    slug: 'concepts/methodology',
  },
  {
    dest: join(CONTENT_DOCS, 'concepts', 'faq.md'),
    src: join(REPO_ROOT, 'docs', 'faq.md'),
    frontmatter: { title: 'FAQ', sourceDoc: 'docs/faq.md' },
    slug: 'concepts/faq',
  },
  {
    dest: join(CONTENT_DOCS, 'reference', 'repository-structure.md'),
    src: join(REPO_ROOT, 'docs', 'repository-structure.md'),
    frontmatter: { title: 'Repository Structure', sourceDoc: 'docs/repository-structure.md' },
    slug: 'reference/repository-structure',
  },
  {
    dest: join(CONTENT_DOCS, 'reference', 'glossary.md'),
    src: join(REPO_ROOT, 'docs', 'glossary.md'),
    frontmatter: { title: 'Glossary', sourceDoc: 'docs/glossary.md' },
    slug: 'reference/glossary',
  },
];

// ── Setup: pages must exist (generated by prebuild/sync:docs) ─────────────────

beforeAll(() => {
  // Pages should already exist from prebuild; if not, generate them
  const pagesExist = MIGRATED_PAGES.every((p) => existsSync(p.dest));
  if (!pagesExist) {
    execSync('node scripts/sync-docs.mjs', { cwd: SITE_ROOT, stdio: 'pipe' });
  }
});

// ── Existence checks ──────────────────────────────────────────────────────────

describe('sync-docs: output files exist', () => {
  for (const page of MIGRATED_PAGES) {
    it(`generated file exists: ${page.slug}`, () => {
      expect(existsSync(page.dest)).toBe(true);
    });
  }

  it('image copied to src/assets/3-ironman.png', () => {
    expect(existsSync(join(ASSETS_DIR, '3-ironman.png'))).toBe(true);
  });

  it('.synced-manifest.json exists outside collection root', () => {
    expect(existsSync(MANIFEST_PATH)).toBe(true);
  });
});

// ── Frontmatter checks ────────────────────────────────────────────────────────

describe('sync-docs: frontmatter injection', () => {
  for (const page of MIGRATED_PAGES) {
    it(`${page.slug}: has title frontmatter`, () => {
      const content = readFileSync(page.dest, 'utf8');
      expect(content).toMatch(/^---\n/);
      expect(content).toContain(`title: '${page.frontmatter.title}'`);
    });

    it(`${page.slug}: has sourceDoc frontmatter`, () => {
      const content = readFileSync(page.dest, 'utf8');
      expect(content).toContain(`sourceDoc: '${page.frontmatter.sourceDoc}'`);
    });

    it(`${page.slug}: frontmatter closes with ---`, () => {
      const content = readFileSync(page.dest, 'utf8');
      const match = content.match(/^---\n[\s\S]*?\n---/);
      expect(match).not.toBeNull();
    });
  }
});

// ── H1 strip ──────────────────────────────────────────────────────────────────

describe('sync-docs: H1 stripping', () => {
  for (const page of MIGRATED_PAGES) {
    it(`${page.slug}: leading H1 is stripped from body`, () => {
      const src = readFileSync(page.src, 'utf8');
      const dest = readFileSync(page.dest, 'utf8');

      // Find the H1 in the source (outside fences — for methodology the H1 is line 1)
      const srcH1 = src.split('\n').find((l) => /^# /.test(l));
      expect(srcH1).toBeDefined();

      // The generated page body (after frontmatter) should NOT start with the H1
      const bodyStart = dest.replace(/^---\n[\s\S]*?\n---\n\n?/, '');
      expect(bodyStart.trimStart()).not.toMatch(/^# /);
    });
  }
});

// ── Content loss check ────────────────────────────────────────────────────────

describe('sync-docs: no content loss', () => {
  for (const page of MIGRATED_PAGES) {
    it(`${page.slug}: body content preserved (word count within 5% of source)`, () => {
      const src = readFileSync(page.src, 'utf8');
      const dest = readFileSync(page.dest, 'utf8');

      // Strip the H1 and frontmatter to compare bodies
      const srcBody = src.replace(/^# .+\n/, '').trim();
      const destBody = dest.replace(/^---\n[\s\S]*?\n---\n\n?/, '').trim();

      const srcWords = srcBody.split(/\s+/).length;
      const destWords = destBody.split(/\s+/).length;

      // Allow up to 5% word count difference (link rewrites add/remove some words)
      const ratio = Math.abs(srcWords - destWords) / srcWords;
      expect(ratio).toBeLessThan(0.05);
    });
  }
});

// ── Fence awareness ───────────────────────────────────────────────────────────

describe('sync-docs: fence-aware transform', () => {
  it('methodology.md: all 7 mermaid fences are present', () => {
    const content = readFileSync(MIGRATED_PAGES[0].dest, 'utf8');
    const mermaidCount = (content.match(/^```mermaid$/gm) || []).length;
    expect(mermaidCount).toBe(7);
  });

  it('methodology.md: mermaid fences are closed', () => {
    const content = readFileSync(MIGRATED_PAGES[0].dest, 'utf8');
    const openCount = (content.match(/^```mermaid$/gm) || []).length;
    const closeCount = (content.match(/^```$/gm) || []).length;
    // All fences must be closed
    expect(closeCount).toBeGreaterThanOrEqual(openCount);
  });

  it('methodology.md: # lines inside code fences are not treated as headings', () => {
    const content = readFileSync(MIGRATED_PAGES[0].dest, 'utf8');
    // There should be exactly one H1 stripped (none in body outside fences)
    const bodyAfterFm = content.replace(/^---\n[\s\S]*?\n---\n\n?/, '');
    // Parse fence state to check # lines outside fences
    const lines = bodyAfterFm.split('\n');
    let inFence = false;
    let h1OutsideFence = 0;
    for (const line of lines) {
      if (/^```/.test(line)) { inFence = !inFence; }
      if (!inFence && /^# /.test(line)) { h1OutsideFence++; }
    }
    // No H1 should exist outside fences in the body (it was stripped)
    expect(h1OutsideFence).toBe(0);
  });
});

// ── Link rewrite checks ───────────────────────────────────────────────────────

describe('sync-docs: link rewriting', () => {
  it('faq.md: cross-doc link to methodology rewrites to /concepts/methodology', () => {
    const content = readFileSync(MIGRATED_PAGES[1].dest, 'utf8');
    // The FAQ has: See the [comparison table](aid-methodology.md#9-comparison-with-sdd)
    expect(content).toContain('/concepts/methodology#9-comparison-with-sdd');
    expect(content).not.toContain('](aid-methodology.md');
  });

  it('faq.md: install.md links rewrite to GitHub blob URL', () => {
    const content = readFileSync(MIGRATED_PAGES[1].dest, 'utf8');
    expect(content).toContain('https://github.com/AndreVianna/aid-methodology/blob/master/docs/install.md');
    expect(content).not.toMatch(/\]\(install\.md/);
  });

  it('repository-structure.md: ../CONTRIBUTING.md rewrites to GitHub blob URL', () => {
    const content = readFileSync(MIGRATED_PAGES[2].dest, 'utf8');
    expect(content).toContain('https://github.com/AndreVianna/aid-methodology/blob/master/CONTRIBUTING.md');
    expect(content).not.toContain('../CONTRIBUTING.md');
  });

  it('methodology.md: same-doc anchors are unchanged', () => {
    const content = readFileSync(MIGRATED_PAGES[0].dest, 'utf8');
    // The methodology ToC uses same-doc anchors like (#1-the-pipeline)
    expect(content).toContain('(#1-the-pipeline)');
  });

  it('methodology.md: external https:// links are unchanged', () => {
    const src = readFileSync(MIGRATED_PAGES[0].src, 'utf8');
    const dest = readFileSync(MIGRATED_PAGES[0].dest, 'utf8');
    // Extract all https:// links from source and verify they appear in dest
    const srcHttpsLinks = [...src.matchAll(/\]\((https?:\/\/[^)]+)\)/g)].map((m) => m[1]);
    for (const link of srcHttpsLinks) {
      expect(dest).toContain(link);
    }
  });
});

// ── Image rewrite ─────────────────────────────────────────────────────────────

describe('sync-docs: image relocation', () => {
  it('methodology.md: image reference rewritten to relative ../../../assets/ path', () => {
    const content = readFileSync(MIGRATED_PAGES[0].dest, 'utf8');
    expect(content).toContain('../../../assets/3-ironman.png');
    expect(content).not.toContain('images/3-ironman.png');
  });
});

// ── Manifest checks ───────────────────────────────────────────────────────────

describe('sync-docs: .synced-manifest.json', () => {
  let manifest;

  beforeAll(() => {
    manifest = JSON.parse(readFileSync(MANIFEST_PATH, 'utf8'));
  });

  it('manifest has 4 entries (one per migrated page)', () => {
    expect(manifest.entries).toHaveLength(4);
  });

  it('manifest entries include expected source docs', () => {
    const srcs = manifest.entries.map((e) => e.src);
    expect(srcs).toContain('docs/aid-methodology.md');
    expect(srcs).toContain('docs/faq.md');
    expect(srcs).toContain('docs/repository-structure.md');
    expect(srcs).toContain('docs/glossary.md');
  });

  it('manifest does NOT include install.md or release.md', () => {
    const srcs = manifest.entries.map((e) => e.src);
    expect(srcs).not.toContain('docs/install.md');
    expect(srcs).not.toContain('docs/release.md');
  });

  it('manifest.generator identifies sync-docs.mjs', () => {
    expect(manifest.generator).toContain('sync-docs.mjs');
  });

  it('manifest has no generatedAt field (determinism: no wall-clock timestamps)', () => {
    expect(manifest).not.toHaveProperty('generatedAt');
  });
});

// ── Install.md and release.md exclusion ──────────────────────────────────────

describe('sync-docs: excluded files not generated', () => {
  it('guides/installation.md is NOT generated by sync-docs', () => {
    // install.md must not create a file in guides/
    expect(existsSync(join(CONTENT_DOCS, 'guides', 'installation.md'))).toBe(false);
  });
});

// ── Idempotency / drift-check ─────────────────────────────────────────────────

describe('sync-docs: idempotency (drift-check)', () => {
  it('running sync:docs again produces no diff on the owned files', () => {
    // Re-run the sync
    execSync('node scripts/sync-docs.mjs', { cwd: SITE_ROOT, stdio: 'pipe' });

    // Check git diff scoped to the four migrated pages, assets, and manifest
    const scopedPaths = [
      'site/src/content/docs/concepts/methodology.md',
      'site/src/content/docs/concepts/faq.md',
      'site/src/content/docs/reference/repository-structure.md',
      'site/src/content/docs/reference/glossary.md',
      'site/src/assets/',
      'site/scripts/.synced-manifest.json',
    ];

    let diffOutput = '';
    try {
      diffOutput = execSync(
        `git diff --exit-code -- ${scopedPaths.join(' ')}`,
        { cwd: REPO_ROOT, encoding: 'utf8' }
      );
    } catch (err) {
      // git diff --exit-code exits 1 when diff is non-empty
      diffOutput = err.stdout || '';
      throw new Error(
        `Drift detected after re-running sync:docs. Diff output:\n${diffOutput}`
      );
    }

    expect(diffOutput).toBe('');
  });
});
